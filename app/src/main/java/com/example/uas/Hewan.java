package com.example.uas;

public class Hewan {
    String nama, kaki;

    public Hewan(String nama, String kaki) {
        this.nama = nama;
        this.kaki = kaki;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getKaki() {
        return kaki;
    }

    public void setKaki(String kaki) {
        this.kaki = kaki;
    }
}
